public class AktinaKiklouRadius {

    private double x, y, z;
    private double π = 3.14;
    private double r = 7.5;

    public double perimetros(double x, double r, double π) {

        return 2 * r * π;

    }

    public double area(double π, double r, double x) {

        return π * r * r;
    }

}
